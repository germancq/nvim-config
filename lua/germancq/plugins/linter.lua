return {
	"mfussenegger/nvim-lint",
	event = {
		"BufReadPre",
		"BufNewFile",
	},
	config = function()
		local lint = require("lint")

		lint.linters_by_ft = {
			javascript = { "eslint_d" },
			typescript = { "eslint_d" },
			javascriptreact = { "eslint_d" },
			typescriptreact = { "eslint_d" },
			svelte = { "eslint_d" },
			python = { "pylint" },
			verilog = { "verilator" },
		}

		require("lint").linters.verible = {
			cmd = "verible-verilog-lint",
			stdin = true,
			args = { "--rules", "all" }, -- Configura reglas o flags si es necesario
			stream = "stdout",
			ignore_exitcode = true, -- No fallará si el linter retorna un código de error
			parser = require("lint.parser").from_errorformat("%f:%l:%c: %m"),
		}

		local lint_augroup = vim.api.nvim_create_augroup("lint", { clear = true })

		vim.api.nvim_create_autocmd({ "BufEnter", "BufWritePost", "InsertLeave" }, {
			group = lint_augroup,
			callback = function()
				lint.try_lint()
			end,
		})

		vim.keymap.set("n", "<leader>l", function()
			lint.try_lint()
		end, { desc = "Trigger linting for current file" })
	end,
}
